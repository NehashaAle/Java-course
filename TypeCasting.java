public class TypeCasting {
    public static void main(String arg[]) {
        int a = 45;

        byte b = (byte) a; // Casting

        float f = 5.6f;
        double d = f; // Conversion

        int x = 10;
        int y = 20;

        int result = x * y;
        System.out.println(result);

        System.out.println(b);
        System.out.println(d);
    }
}
